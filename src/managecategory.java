    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed

        String name = txtname.getText();
        if (!ValidateForm()) {
            JOptionPane.showMessageDialog(null, "All Field required ");

        } else {

            try {
                Connection con = connectionprovider.getCon();
                PreparedStatement pst = con.prepareStatement("insert into category (name) values(?)");
                pst.setString(1, name);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "category added sucessfully");
                setVisible(false);
                new managecategory().setVisible(true);
            } catch (NumberFormatException np) {
                JOptionPane.showMessageDialog(null, "Please Fill Required Fields Correctly !");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "An Error Occurred");
            }

            btnupdate.setEnabled(false);
        }
    }//GEN-LAST:event_btnsaveActionPerformed

