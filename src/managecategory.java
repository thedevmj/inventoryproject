    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        DefaultTableModel model = (DefaultTableModel) tblcategory.getModel();
        try {
            Connection con = connectionprovider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from category");
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("category_pk"), rs.getString("name")});

            }

        }  catch (NumberFormatException np) {
            JOptionPane.showMessageDialog(null, "Please Fill Required Fields Correctly !");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "An Error Occurred");
        }
        btnupdate.setEnabled(true);

    }//GEN-LAST:event_formComponentShown

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        String name = txtname.getText();
        if (!ValidateForm()) {
            JOptionPane.showMessageDialog(null, "All Field required ");
        } else {

            try {
                Connection con = connectionprovider.getCon();
                PreparedStatement pst = con.prepareStatement("update category set name=? where category_pk=?");
                pst.setString(1, name);
                pst.setInt(2, categorypk);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "category Updated sucessfully");
                setVisible(false);
                new managecategory().setVisible(true);
            }  catch (NumberFormatException np) {
            JOptionPane.showMessageDialog(null, "Please Fill Required Fields Correctly !");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "An Error Occurred");
        }
            btnsave.setEnabled(true);
        }

    }//GEN-LAST:event_btnupdateActionPerformed

